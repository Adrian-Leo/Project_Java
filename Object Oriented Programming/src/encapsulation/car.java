public class car {
    private String brand,indirect_injection,fuel,transmission;
    private int door;
 
    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getIndirect_injection() {
        return indirect_injection;
    }

    public void setIndirect_injection(String indirect_injection) {
        this.indirect_injection = indirect_injection;
    }

    public String getFuel() {
        return fuel;
    }

    public void setFuel(String fuel) {
        this.fuel = fuel;
    }

    public int getDoor() {
        return door;
    }

 

    public void setDoor(int door) {
        this.door = door;
    }

    public String getTransmission() {
        return transmission;
    }

    public void setTransmission(String transmission) {
        this.transmission = transmission;
    }

    

}

 

